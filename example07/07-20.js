// 대소 관계 비교
5 > 0; // true
5 > 5; // false
5 >= 5; // true
5 <= 5; // true

/*
    대소 관계 비교 연산자   예제        설명                    부수 효과
    >                      x > y       x가 y보다 크다          X
    <                      x < y       x가 y보다 작다          X
    >=                     x >= y      x가 y보다 크거나 같다   X
    <=                     x <= y      x가 y보다 작거나 같다   X
*/