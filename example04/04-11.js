var score = 80; // 변수 선언과 값의 할당
score = 90; // 값의 재할당


/*
    재할당은 이미 값이 할당되어 있는 변수에 새로운 값을 또 다시 할당하는 것을 말한다.

    var 키워드로 선언한 변수는 값을 재할당할 수 있다.
    재할당은 현재 변수에 저장된 값을 버리고 새로운 값을 저장하는 것이다.
    var 키워드로 선언한 변수는 선언과 동시에 undefined로 초기화되기 때문에 엄밀히 말하면 변수에 처음으로 값을 할당하는 것도 사실은 재할당이다.

    만약 값을 재할당할 수 없어서 변수에 저장된 값을 변경할 수 없다면 변수가 아니라 상수라고 한다.
*/